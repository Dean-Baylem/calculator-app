import math

# Functions

def add(x, y):
    return x + y


def subtract(x, y):
    return x - y


def multiply(x, y):
    return x * y


def divide(x, y):
    return x / y


def square_root(x):
    return math.sqrt(x)


def square(x):
    return x * x


def power(x, y):
    return pow(x, y)


def log10(x):
    return math.log10(x)


def factorial(x):
    return math.factorial(x)


